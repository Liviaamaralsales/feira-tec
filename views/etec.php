                <p>A Escola Técnica Estadual Professora Maria Cristina Medeiros (ETEC MCM), localizada em Ribeirão Pires, São Paulo, é uma instituição pública de ensino vinculada ao Centro Paula Souza. Carrega seu nome em homenagem a sua diretora falecida Maria Cristina Medeiros. Inaugurada com a missão de oferecer ensino técnico de qualidade e formar profissionais preparados para os desafios do futuro.​</p>
